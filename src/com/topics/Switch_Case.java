package com.topics;

public class Switch_Case {

	public static void main(String[] args) {

		switch ("Hulk") {								//variable given

		case "captain america":							//condition declared
			System.out.println("it is captain america");
			break;
			
		case "super man":
			System.out.println("it is superman");
			break;

		case "batman":
			System.out.println("it is batman");
			break;

		default:
			System.out.println("Hulk is the superhero i thought of");
		}
		
	}

}
